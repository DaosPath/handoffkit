DemoCase C(
    const char* id,
    const char* domain,
    const char* title,
    const char* task,
    const char* context,
    std::vector<std::string> constraints,
    std::vector<std::string> success,
    std::vector<std::string> decisions,
    std::vector<std::string> files,
    int difficulty
) {
    DemoCase c;
    c.id = id;
    c.domain = domain;
    c.title = title;
    c.task = task;
    c.context = context;
    c.constraints = std::move(constraints);
    c.success_criteria = std::move(success);
    c.seed_decisions = std::move(decisions);
    c.seed_files = std::move(files);
    c.difficulty = difficulty;
    return c;
}

