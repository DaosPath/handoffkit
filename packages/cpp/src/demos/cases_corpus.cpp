const std::vector<DemoCase>& case_corpus() {
    static const std::vector<DemoCase> kCases = build_corpus();
    return kCases;
}

const DemoCase* find_case(std::string_view id) {
    for (const auto& c : case_corpus()) {
        if (c.id == id) {
            return &c;
        }
    }
    return nullptr;
}

std::vector<const DemoCase*> cases_for_domain(std::string_view domain) {
    std::vector<const DemoCase*> out;
    for (const auto& c : case_corpus()) {
        if (c.domain == domain) {
            out.push_back(&c);
        }
    }
    return out;
}

std::size_t case_corpus_size() {
    return case_corpus().size();
}

